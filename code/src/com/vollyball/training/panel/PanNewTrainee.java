    private void savePlayerLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_savePlayerLabelMouseClicked
        // TODO add your handling code here:
        switch (type) {
            case 1:
                String msg = validateFields();
                if (!msg.isEmpty()) {
                    JOptionPane.showMessageDialog(this, msg);
                } else {
                    Trainee trainee = new Trainee();
                    trainee.setBatchId(team1combo1.getSelectedIndex());
                    trainee.setName(txtTraineeName.getText());

                    int id = batchDao.insertTrainee(trainee);

                    if (id != 0) {
                        Controller.createTraineeDailog.close();
                        JOptionPane.showMessageDialog(this, "Trainee '" + txtTraineeName.getText() + "' Added Successfully");
                    } else {
                        JOptionPane.showMessageDialog(this, "Failed to add Trainee");
                    }
                }
                break;
            case 2:
                String msg2 = validateFields();
                if (!msg2.isEmpty()) {
                    JOptionPane.showMessageDialog(this, msg2);
                } else {
                    Trainee trainee = new Trainee();
                    trainee.setBatchId(batchId);
                    trainee.setName(txtTraineeName.getText());

                    int id = batchDao.insertTrainee(trainee);

                    if (id != 0) {
                        Controller.panBatchTraineeList.setRow(batchId);
                        Controller.createTraineeDailog.close();
                        JOptionPane.showMessageDialog(this, "Trainee '" + txtTraineeName.getText() + "' Added Successfully");

                    } else {
                        JOptionPane.showMessageDialog(this, "Failed to add Trainee");
                    }
                }
                break;
            case 3:
                String msg3 = validateFields();
                if (!msg3.isEmpty()) {
                    JOptionPane.showMessageDialog(this, msg3);
                } else {
                    Trainee trainee = new Trainee();
                    trainee.setId(traineeId);
                    trainee.setBatchId(batchId);
                    trainee.setName(txtTraineeName.getText());
                    int id = batchDao.updateTrainee(trainee);
                    if (id != 0) {
                        Controller.panBatchTraineeList.setRow(batchId);
                        Controller.createTraineeDailog.close();
                        JOptionPane.showMessageDialog(this, "Trainee '" + txtTraineeName.getText() + "' Updated Successfully");

                    } else {
                        JOptionPane.showMessageDialog(this, "Failed to update Trainee");
                    }
                }
            default:
                Controller.panBatchTraineeList.setRow(batchId);
                break;
        }

    }//GEN-LAST:event_savePlayerLabelMouseClicked

    public String validateFields() {
        String msg = "";

        int k = 1;

        if (txtTraineeName.getText().equals("")) {
            msg = msg + "Name Cannot be Blank";
            txtTraineeName.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.red));
        }

        if (team1combo1.getSelectedItem().equals("Select")) {
            msg = msg + "Select Batch";
            team1combo1.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.red));
        }

        return msg;

    }

