    public PanNewTrainee(Trainee trainee) {
        initComponents();
        this.trainee = trainee;
        batchId = trainee.getBatchId();
        traineeId = trainee.getId();
        List<Batch> batchList = batchDao.getBatchList();
        List<Trainee> traineeList = batchDao.getTraineeList(trainee.getBatchId());
        batchMap = new LinkedHashMap<>();
        for (Batch batch : batchList) {
            if (trainee.getBatchId() == batch.getId()) {
                batchMap.put(batch.getName(), batch.getId());
                team1combo1.addItem(batch.getName());
            }
        }
        String traineeName = "";
        for (Trainee t : traineeList) {
            if (t.getId() == trainee.getId()) {
                traineeName = trainee.getName();
            }
        }
        txtTraineeName.setText(traineeName);
        type = 3;
    }

