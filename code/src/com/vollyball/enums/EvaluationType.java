package com.vollyball.enums;

/**
 *
 * @author nishant.vibhute
 */
public enum EvaluationType {

    LIVE(1, "Live"),
    POST(2, "POST");

    int id;
    String name;

    private EvaluationType(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
