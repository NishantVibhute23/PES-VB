    public void setPlayer(java.awt.event.KeyEvent evt, int pos) {
        JTextField jf = (JTextField) evt.getSource();
        String item = jf.getText();
        if (item.length() > 1) {
            if (Controller.panMatchSet.ChestMap.containsKey(item)) {
                for (Map.Entry<Integer, Player> entry : rallyPositionMap.entrySet()) {
                    if (entry.getKey() != 7) {
                        if (!rallyPositionMap.get(pos).getChestNo().equals(jf.getText())) {
                            Player pl = entry.getValue();
                            if (pl.getChestNo().equals(jf.getText())) {
                                jf.setText("");
                            }
                        }
                    }
                }
            } else {
                jf.setText("");
            }
        }
    }

