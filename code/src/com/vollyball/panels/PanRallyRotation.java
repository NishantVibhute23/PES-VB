    private void lblOkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblOkMouseClicked
        // TODO add your handling code here:
        boolean isValidate = true;
        boolean isExist = true;
        for (JTextField jf : posTextFileds) {
            if (jf.getText().equals("")) {
                isValidate = false;
                jf.setBorder(new LineBorder(Color.RED));

            } else {
                if (Controller.panMatchSet.ChestMap.get(jf.getText()) == null) {
                    isExist = false;
                    jf.setBorder(new LineBorder(Color.RED));

                }
            }
        }
        if (isValidate && isExist) {
            int i = 1;
            for (JTextField jf : posTextFileds) {
                Controller.panMatchSet.rallyPositionMap.put(i, Controller.panMatchSet.ChestMap.get(jf.getText()));
                i++;
            }
            obj.close();
        } else if (!isExist) {
            JOptionPane.showMessageDialog(this, "Player Chest number not Exist");
        } else {
            JOptionPane.showMessageDialog(this, "Players cannot be empty");
        }

    }//GEN-LAST:event_lblOkMouseClicked

