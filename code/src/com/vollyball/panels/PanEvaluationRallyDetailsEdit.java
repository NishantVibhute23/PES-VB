    public void checkNumber(java.awt.event.KeyEvent evt, int type) {
        ((JTextField) evt.getSource()).setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c))) {
            evt.consume();
        }
    }

    public String getSkillCode(String skill) {
        String code = "";
        switch (skill) {
            case "Service":
                code = "Q";
                break;
            case "Reception":
                code = "W";
                break;
            case "Attack":
                code = "E";
                break;
            case "Set":
                code = "R";
                break;
            case "Block":
                code = "T";
                break;
            case "Defence":
                code = "Y";
                break;
            default:
                break;

        }
        return code;
    }

    public String getRating(int rate) {
        String code = "";
        switch (rate) {
            case 1:
                code = "Z";
                break;
            case 2:
                code = "X";
                break;
            case 3:
                code = "C";
                break;
            case 4:
                code = "V";
                break;
            case 5:
                code = "B";
                break;

            default:
                code = "C";
                break;

        }
        return code;
    }

